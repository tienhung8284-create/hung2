'''
Chương trình tính score
Tác giả: Trương Tiến Hùng
'''

score = 0   # Khai báo biến 
print(score)

score = score + 10 # Tính toán và gán giá trị cho biến
print(f'Score: {score}')

score += 10        # Tương đương = score + 10
print(f'Score: {score}')

score -= 5      # Tương đương = score - 10 
print(f'Score: {score}')